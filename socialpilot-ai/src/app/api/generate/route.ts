function getRandomResponse(platform: string, contentType: string, tone: string, topic: string): string {
  const platformResponses = demoResponses[platform]
  if (!platformResponses) return '–ö–æ–Ω—Ç–µ–Ω—Ç –¥–ª—è –≤—ã–±—Ä–∞–Ω–Ω–æ–π –ø–ª–∞—Ç—Ñ–æ—Ä–º—ã –ø–æ–∫–∞ –Ω–µ–¥–æ—Å—Ç—É–ø–µ–Ω.'
  
  const typeResponses = platformResponses[contentType]
  if (!typeResponses) return '–ö–æ–Ω—Ç–µ–Ω—Ç –¥–ª—è –≤—ã–±—Ä–∞–Ω–Ω–æ–≥–æ —Ç–∏–ø–∞ –ø–æ–∫–∞ –Ω–µ–¥–æ—Å—Ç—É–ø–µ–Ω.'
  
  const toneResponses = typeResponses[tone]
  if (!toneResponses || toneResponses.length === 0) return '–ö–æ–Ω—Ç–µ–Ω—Ç –¥–ª—è –≤—ã–±—Ä–∞–Ω–Ω–æ–≥–æ —Ç–æ–Ω–∞ –ø–æ–∫–∞ –Ω–µ–¥–æ—Å—Ç—É–ø–µ–Ω.'
  
  const randomIndex = Math.floor(Math.random() * toneResponses.length)
  return toneResponses[randomIndex].replace(/{topic}/g, topic)
}

